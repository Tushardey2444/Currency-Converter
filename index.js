let dropdowns = document.getElementsByTagName('select');
let button = document.querySelector('.btnDiv button');

for (let select of dropdowns) {
    for (let country in countryList) {
        let optionEle = document.createElement('option');
        optionEle.innerText = country;
        optionEle.value = country;
        select.appendChild(optionEle);
        if (country === 'USD' && select.id === 'fromSelect') {
            optionEle.selected = true;
        }
        if (country === 'INR' && select.id === 'toSelect') {
            optionEle.selected = true;
        }
    }
    select.addEventListener('change', (event) => {
        updateFLag(event.target);
    })
}
const updateFLag = (element) => {
    let country = element.value;
    let value = countryList[country];
    let url = `https://flagsapi.com/${value}/shiny/64.png`;
    let img = element.parentElement.parentElement.querySelector('img');
    img.src = url;
}

button.addEventListener('click', async(event) => {
    event.preventDefault();
    let input = document.querySelector('.amount input');
    let value = input.value;
    let resultTag = document.querySelector('.result h3');
    if (value === '') {
        value = 1;
    }
    let selectedValue1 = '';
    let selectedValue2 = '';
    for (let select of dropdowns) {
        if (select.name === 'fromSelect') {
            selectedValue1 = select.value;
        } else {
            selectedValue2 = select.value;
        }
    }
    let url = `https://cdn.jsdelivr.net/gh/fawazahmed0/currency-api@1/latest/currencies/${selectedValue1.toLowerCase()}/${selectedValue2.toLowerCase()}.json`;
    let currencyValue = await fetch(url);
    let result = await currencyValue.json();
    resultTag.textContent = value + ' ' + selectedValue1 + ' = ' + (result[selectedValue2.toLowerCase()] * value) + ' ' + selectedValue2;
});